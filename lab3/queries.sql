-- запрос 1
SELECT О."КОД", В."ДАТА"
FROM "Н_ОЦЕНКИ" О
         INNER JOIN "Н_ВЕДОМОСТИ" В ON О."КОД" = В."ОЦЕНКА"
WHERE О."КОД" < 'неявка'
  AND В."ДАТА" = '1998-01-05'
  AND В."ДАТА" > '2010-06-18';

-- запрос 2
SELECT Л."ФАМИЛИЯ",
       В."ЧЛВК_ИД",
       С."ЧЛВК_ИД"
FROM "Н_ЛЮДИ" Л
         LEFT JOIN "Н_СЕССИЯ" С ON Л."ИД" = С."ЧЛВК_ИД"
         LEFT JOIN "Н_ВЕДОМОСТИ" В ON Л."ИД" = В."ЧЛВК_ИД"
WHERE Л."ИД" > 100865
  AND В."ЧЛВК_ИД" = 105590;